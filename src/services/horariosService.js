import API_CONFIG, { apiFetch } from '@/config/api'
import { authService } from './authService'

const parsearErrorAPI = async (response) => {
  try {
    const error = await response.json()
    return {
      message: error.message || 'Error en la operaci√≥n',
      errors: error.errors || null
    }
  } catch (e) {
    return {
      message: 'Error de conexi√≥n con el servidor',
      errors: null
    }
  }
}

export const horariosService = {
  // Listar horarios de una cl√≠nica
  async listarPorClinica(idClinica) {
    try {
      const token = authService.getToken()
      
      if (!token) {
        throw new Error('No hay sesi√≥n activa')
      }

      const response = await apiFetch(`horarios-clinica/clinica/${idClinica}`, {
        method: 'GET',
        headers: {
          'Authorization': `Bearer ${token}`
        }
      })

      if (!response.ok) {
        const error = await parsearErrorAPI(response)
        throw error
      }

      const result = await response.json()
      return result.data || []
    } catch (error) {
      console.error('Error en listarPorClinica:', error)
      throw error
    }
  },

  // Actualizar horarios de una cl√≠nica
  async actualizarHorarios(idClinica, horarios) {
    try {
      const token = authService.getToken()
      
      if (!token) {
        throw new Error('No hay sesi√≥n activa')
      }

      console.log('üì§ Actualizando horarios:', idClinica, horarios)

      const response = await apiFetch(`horarios-clinica/clinica/${idClinica}`, {
        method: 'PUT',
        headers: {
          'Authorization': `Bearer ${token}`
        },
        body: JSON.stringify(horarios)
      })

      if (!response.ok) {
        const error = await parsearErrorAPI(response)
        throw error
      }

      const result = await response.json()
      console.log('‚úÖ Horarios actualizados:', result)
      return result
    } catch (error) {
      console.error('Error en actualizarHorarios:', error)
      throw error
    }
  },

  // Obtener un horario espec√≠fico de una cl√≠nica
  async obtenerPorId(idHorario, idClinica) {
    try {
      const token = authService.getToken()
      
      if (!token) {
        throw new Error('No hay sesi√≥n activa')
      }

      const response = await apiFetch(`horarios-clinica/${idHorario}/clinica/${idClinica}`, {
        method: 'GET',
        headers: {
          'Authorization': `Bearer ${token}`
        }
      })

      if (!response.ok) {
        const error = await parsearErrorAPI(response)
        throw error
      }

      const result = await response.json()
      return result.data
    } catch (error) {
      console.error('Error en obtenerPorId:', error)
      throw error
    }
  }
}

